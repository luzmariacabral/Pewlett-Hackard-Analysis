SELECT DISTINCT ON (emp_no) emp_no,
last_name,
first_name,
title
INTO unique_titles
FROM retirement_titles
ORDER BY emp_no, to_date DESC;

-- Use Dictinct with Orderby to remove duplicate rows
SELECT COUNT(title), 
	title
INTO retiring_titles
FROM unique_titles
GROUP BY title
ORDER BY COUNT DESC;

